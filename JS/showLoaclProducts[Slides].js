      function applyFilters() {
        const selectedBrands = Array.from(
          document.querySelectorAll(".brand-filter:checked")
        ).map((cb) => cb.value.toLowerCase());

        const selectedCategories = Array.from(
          document.querySelectorAll(".category-filter:checked")
        ).map((cb) => cb.value.toLowerCase());

        const minPrice = parseFloat(document.getElementById("minPrice").value);
        const maxPrice = parseFloat(document.getElementById("maxPrice").value);

        const searchValue = document
          .getElementById("search-input")
          .value.trim()
          .toLowerCase();

        const validMinPrice = isNaN(minPrice) ? 0 : minPrice;
        const validMaxPrice = isNaN(maxPrice) ? Infinity : maxPrice;

        const allProducts = document.querySelectorAll(".all-products .card");
        let anyVisible = false;

        allProducts.forEach((productElement, index) => {
          const product = localProducts[index];
          const productPrice = parseFloat(
            product.price.toString().replace(/[^\d.-]/g, "")
          );
          const productTitle = product.title.toLowerCase();

          const matchesBrand =
            selectedBrands.length === 0 ||
            selectedBrands.includes(product.brand.toLowerCase());
          const matchesCategory =
            selectedCategories.length === 0 ||
            selectedCategories.includes(product.category.toLowerCase());
          const matchesPrice =
            productPrice >= validMinPrice && productPrice <= validMaxPrice;
          const matchesSearch =
            searchValue === "" || productTitle.includes(searchValue);

          const shouldShow =
            matchesBrand && matchesCategory && matchesPrice && matchesSearch;

          if (shouldShow) {
            productElement.style.display = "block";
            anyVisible = true;
          } else {
            productElement.style.display = "none";
          }
        });

        const noItemFoundMessage = document.getElementById("noItemFound");
        if (noItemFoundMessage) {
          noItemFoundMessage.style.display = anyVisible ? "none" : "block";
        }
      }


      document
        .getElementById("search-input")
